package basics;

public class DataTypesExample {

	public static void main(String[] args) {
		int number = 78;
		System.out.println("value is" + number);
		byte byteNumber = 18;
		System.out.println("value is " + byteNumber);
		long longNumber = 12345678;
		System.out.println("value is " + longNumber);
		short shortNumber = 4567;
		System.out.println("value is " + shortNumber);
		float floatNumber = 0.143f;
		System.out.println("value is " + floatNumber);
		double doubleNumber = 0.123456789d;
		System.out.println("value is "+ doubleNumber);
		
		
	}

}
